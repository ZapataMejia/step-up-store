// ───────────────────────────────────────────────────────────────────────────
// CATÁLOGO DE PRODUCTOS — STEP UP STORE
//
// Cómo agregar un producto:
//   1. Sube la(s) foto(s) a  public/images/products/<categoria>/  (ej: tenis/yeezy-350.jpg)
//   2. Agrega un objeto al arreglo `products` de abajo con sus datos.
//   3. Listo: aparece automáticamente en su categoría, en el buscador y con
//      el botón de WhatsApp ya armado con nombre, referencia, color y precio.
//
// Campos:
//   - category: debe coincidir con un slug de `categoryList` (tenis, ropa, ...)
//   - price:    número en pesos (sin puntos). Ej: 350000  ->  se muestra $350.000
//   - colors:   lista de colores con nombre y (opcional) un hex para el círculo
//   - images:   rutas relativas a /public. La primera es la principal.
// ───────────────────────────────────────────────────────────────────────────

export type CategorySlug =
  | "tenis"
  | "ropa"
  | "deportivo"
  | "accesorios"
  | "hogar"
  | "cuidado";

export type Gender = "Hombre" | "Mujer" | "Unisex" | "Niños";

export interface ProductColor {
  name: string;
  hex?: string;
  /** Foto de este color. Si se define, al elegir el color la tarjeta cambia la imagen. */
  img?: string;
}

export interface Product {
  id: string;
  name: string;
  brand?: string;
  category: CategorySlug;
  gender?: Gender;
  reference?: string;
  price: number;
  images: string[];
  colors?: ProductColor[];
  sizes?: string[];
  description?: string;
  /** Palabras extra para que el buscador lo encuentre (modelos, sinónimos, etc.) */
  tags?: string[];
  featured?: boolean;
  /** Marca como "Agotado" si no hay stock. */
  soldOut?: boolean;
}

export interface CategoryMeta {
  slug: CategorySlug;
  title: string;
  /** Etiqueta corta para chips/menús. */
  short: string;
  description: string;
  /** Frase para la cabecera de la página de categoría. */
  headline: string;
  emoji: string;
  /** Filtros de género que tiene sentido mostrar en esta categoría. */
  genders?: Gender[];
}

